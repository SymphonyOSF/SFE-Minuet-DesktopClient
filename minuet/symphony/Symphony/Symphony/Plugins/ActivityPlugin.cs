        bool anyKeyPressed()
        {
            bool wasKeyPressed = false;
            // need to loop through all key otherwise if multiple keys are hit then event
            // would be raised on next period.
            foreach (System.Int32 i in Enum.GetValues(typeof(Keys)))
            {
                int x = GetAsyncKeyState(i);
                // detect key down or up.
                if ((x == 1) || (x == Int16.MinValue))
                    wasKeyPressed = true;
            }

            return wasKeyPressed;
        }

        public void Shutdown()
        {
            timer.Dispose();
        }

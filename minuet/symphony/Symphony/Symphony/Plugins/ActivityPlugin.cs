        [JavaScriptPluginMember(Name = "onActivity")]
        public event JavaScriptPluginCallback onActivity;

        [StructLayout(LayoutKind.Sequential)]
        public struct POINT
        {
            public int X;
            public int Y;

            public POINT(int x, int y)
            {
                this.X = x;
                this.Y = y;
            }

            public POINT(System.Drawing.Point pt) : this(pt.X, pt.Y) { }

            public static implicit operator System.Drawing.Point(POINT p)
            {
                return new System.Drawing.Point(p.X, p.Y);
            }

            public static implicit operator POINT(System.Drawing.Point p)
            {
                return new POINT(p.X, p.Y);
            }
        }

        [DllImport("user32.dll", SetLastError = true)]
        [return: MarshalAs(UnmanagedType.Bool)]
        static extern bool GetCursorPos(out POINT lpPoint);

        [DllImport("User32.dll")]
        private static extern short GetAsyncKeyState(System.Int32 vKey);

        int currX = -1;
        int currY = -1;

        System.Threading.Timer timer;

        double checkIntervalInSecs = 5.0;

        public void Initialize(IApplication application)
        {
            timer = new System.Threading.Timer(onTimer, null, TimeSpan.Zero, 
                TimeSpan.FromSeconds(checkIntervalInSecs));
        }

        // check for mouse position changes every 60 seconds
        void onTimer(object state)
        {
            if (anyMouseMovement() || anyKeyPressed())
            {
                var evnt = onActivity;
                if (evnt != null)
                    evnt();
            }            
        }

        bool anyMouseMovement()
        {
            POINT p;
            if (GetCursorPos(out p))
            {
                if (p.X != currX || p.Y != currY)
                {
                    bool isCurrValueInit = (currX == -1 && currY == -1);

                    currX = p.X;
                    currY = p.Y;

                    if (!isCurrValueInit)
                        return true;
                }
            }

            return false;
        }


namespace Paragon.Plugins.Notifications.Contracts
{
    public class NotificationOptions
    {
        public NotificationOptions()
        {
            BackgroundColor = "505050";
            BlinkColor = "ffffff";
            CanBlink = false;
            CanPlaySound = false;
            IsPersistent = false;
            Group = "default";
            TemplateType = TemplateType.Basic;
        }

        public string BackgroundColor { get; set; }
        public string BlinkColor { get; set; }
        public ButtonOption[] Buttons { get; set; }
        public string Callback { get; set; }
        public string CallbackArg { get; set; }
        public bool CanBlink { get; set; }
        public bool CanPlaySound { get; set; }
        public string IconUrl { get; set; }
        public string ImageUrl { get; set; }
        public bool IsClickable { get; set; }
        public bool IsPersistent { get; set; }
        public string Group { get; set; }
        public string Message { get; set; }
        public string Title { get; set; }
        public TemplateType TemplateType { get; set; }
        public int Priority { get; set; }
        public string SoundFile { get; set; }
    }
}